<div id="c">
<div id="list1">
 <p>
     <ul>
    <br>The solver plays by 
    <br>1) selecting a clue, 
    <br>2) thinking up possible answers to the clue,
    <br>3) selecting an answer from among the possible ones, and 
    <br>4) entering the answer in the grid.

    <br>How to enter an answer in the grid:
    
    <li>Place the first letter of an answer for a specific clue in the grid,</li> 
    <li>starting with the numbered white square that corresponds to the clue's type (across or down) and number.</li>
    <li>Continue placing letters in sequential order in the white squares that follow, one for each white square, </li>
    <li>until either the grid perimeter or a black box is reached.</li>
    <li>The game ends when every white square in the grid is filled in with an answer that the solver believes to be correct.</li>
    <br>That's all there is to it!
</ul>
